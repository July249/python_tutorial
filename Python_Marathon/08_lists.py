
friends = ['Mike', 'John', 'Tom', 'Karen', 'Jim']

print(friends) # ['Mike', 'John', 'Tom', 'Karen', 'Jim']
print(friends[0]) # Mike
print(friends[1]) # John
print(friends[2]) # Tom
print(friends[-2]) # Karen
print(friends[-1]) # Jim
print(friends[3:]) # ['Karen', 'Jim']
print(friends[1:3]) # ['John', 'Tom']

friends[0] = 'Kevin'
print(friends[0]) # Kevin